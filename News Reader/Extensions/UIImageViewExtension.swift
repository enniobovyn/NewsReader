import Foundation
import UIKit

extension UIImageView {
    
    func load(url: URL) {
        
        let session = URLSession.shared
        session.dataTask(with: url) { (data, response, error) in
            if let data = data, let image = UIImage(data: data) {
                DispatchQueue.main.async {
                    self.image = image
                }
            }
        }.resume()
        
    }
    
}
